    def get_company_name(self, scrip):
        """Get company name for a scrip"""
        return self.stocks.get(scrip.upper(), "")
    
    def find_scrip_in_text(self, text):
        """Find mentioned scrips in text"""
        text_upper = text.upper()
        found_scrips = []
        
        for scrip, company in self.stocks.items():
            if scrip in text_upper or company.upper() in text_upper:
                found_scrips.append(scrip)
        
        return found_scrips
